#include "3-calc.h"

/**
 * op_add - A function that gets the sum of two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The sum of these two integers
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A function that gets the difference of two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The difference of these two integers
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A function that gets the product of two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The product of these two integers
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - A function that gets the division of two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The result of the division of these two integers
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - A function that gets the remainder of the division of two integers
 * @a: The first integer
 * @b: The second integer
 *
 * Return: The remainder of the division of these two integers
*/

int op_mod(int a, int b)
{
	return (a % b);
}
